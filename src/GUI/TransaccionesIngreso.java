    private void JBConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBConsultarActionPerformed
        OperarcionesCRUD op = OperarcionesCRUD.getInstance();
        String cedula;
        cedula = txtCedula.getText().toString();

        if (!cedula.equals("")) {
            try {
                ArrayList<Vector<String>> matriz = op.OperacionesTransacciones(cedula);

                for (Vector<String> vector : matriz) {
                    String codigo, nombres, apellidos;

                    codigo = vector.elementAt(0);
                    nombres = vector.get(1);
                    apellidos = vector.elementAt(2);
                    codUsuario = vector.elementAt(3);

                    JLabelSocio.setText(codigo);
                    JLabelNombres.setText(nombres + " " + apellidos);

                }

            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese un Numero de Cedula a Consultar");
            jPanel2.setVisible(false);
            JLabelSocio.setText("");
            JLabelNombres.setText("");
            txtCodigoTransaccion.setText("");
        }


    }//GEN-LAST:event_JBConsultarActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String codigo = txtCodigoTransaccion.getText().trim();
        int validacion = 0;
        String socio = JLabelSocio.getText();
        String nombres = JLabelSocio.getText();
        if (socio.equals("")) {
            validacion++;
        }
        if (nombres.equals("")) {
            validacion++;
        }

        if (validacion == 0) {

            if (!codigo.equals("")) {
                txtCantidad.setText("");
                if (codigo.equals("20")) {
                    LabelTransaccion.setText("Apertura de Cuenta");
                    jPanel2.setVisible(true);
                } else if (codigo.equals("21")) {
                    LabelTransaccion.setText("Deposito");
                    jPanel2.setVisible(true);
                } else if (codigo.equals("22")) {
                    LabelTransaccion.setText("Retiro");
                    jPanel2.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Codigo Incorrecto");
                    txtCodigoTransaccion.setText("");
                    jPanel2.setVisible(false);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Ingrese el Codigo a Procesar");
                jPanel2.setVisible(false);

            }
        } else {
            JOptionPane.showMessageDialog(this, "Realice una consulta primero");
            txtCodigoTransaccion.setText("");
        }


    }//GEN-LAST:event_jButton4ActionPerformed

