    private void JBGuadarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBGuadarActionPerformed
        String ret = "", dep = "", apert = "";
        String saldo, codCliente, concepto, cantidad;

        String fechas = fecha.format(fechaactual);
        codCliente = JLabelSocio.getText();
        cantidad = txtCantidad.getText();
        concepto = LabelTransaccion.getText();

        ArrayList<Vector<String>> matriz = new ArrayList<>();
        if (!cantidad.equals("")) {
            Vector<String> datos = new Vector<>();

            if (concepto.equals("Apertura de Cuenta")) {
                apert = cantidad;
            } else if (concepto.equals("Deposito")) {
                dep = cantidad;
            } else if (concepto.equals("Retiro")) {
                ret = cantidad;
            }

            datos.add(concepto); //0
            datos.add(dep);  //1
            datos.add(ret);  //2            
            datos.add(fechas);  //3
            datos.add(codCliente);  //4
            datos.add(codUsuario);  //5
            datos.add(apert); //6
            matriz.add(datos);
            //System.out.println(matriz);

        } else {
            JOptionPane.showMessageDialog(this, "Ingrese una cantidad");
            JBAlerta1.setVisible(true);
        }

        try {
            OperarcionesCRUD op = OperarcionesCRUD.getInstance();
            op.realizarTransaccion(matriz);
        } catch (SQLException err) {
            err.printStackTrace();
        }


    }//GEN-LAST:event_JBGuadarActionPerformed
