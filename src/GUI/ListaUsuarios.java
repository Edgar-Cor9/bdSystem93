    private boolean cerrado(Object obj) {
        JInternalFrame[] FrmActivo = jDesktopPane1.getAllFrames();
        boolean cerrado = true;
        int i = 0;
        while (i < FrmActivo.length && cerrado) {
            if (FrmActivo[i] == obj) {
                cerrado = false;
            }
            i++;
        }
        return cerrado;
    }

