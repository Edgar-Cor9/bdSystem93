    public void Consulta() {
        Date fechaInicio = jDateChooserInicio.getDate();
        Date fechaFin = jDateChooserFin.getDate();

        if (fechaInicio == null || fechaFin == null) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona ambas fechas");
            return;
        }

        if (fechaInicio.after(fechaFin)) {
            JOptionPane.showMessageDialog(this, "La fecha de inicio no puede ser mayor que la fecha de fin");
            return;
        }

        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        String fecInicio = formato.format(fechaInicio);
        String fecFin = formato.format(fechaFin);

        DefaultComboBoxModel modelo = (DefaultComboBoxModel) jComboBoxUsuario.getModel();
        String user = modelo.getSelectedItem().toString();

        try {
            this.matrizProveedores = op.ReportesProveedor(user, fecInicio, fecFin);

            String[] columnas = {"Ruc", "Nombre", "Apellidos", "Email", "Direcci√≥n", "comentario", "Usuario Registro"};

            DefaultTableModel modeloTabla = new DefaultTableModel(columnas, 0) {
                @Override

                public boolean isCellEditable(int row, int colum) {
                    return false;
                }
            };

            modeloTabla.setRowCount(0);

            for (Vector<String> fila : matrizProveedores) {
                modeloTabla.addRow(fila);
            }
            jTableProveedor.setModel(modeloTabla);
            jTableProveedor.setVisible(true);

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public void LimpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTableProveedor.getModel();
        modelo.setRowCount(0);
        jTableProveedor.setVisible(false);
    }

