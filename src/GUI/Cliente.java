    private void JBActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBActualizarActionPerformed

        ArrayList<Vector<String>> matriz = new ArrayList<>();

        String fechas = fecha.format(fechaactual);
        String nombres, apellidos, edad, correo, usuarios, fechaact, cedula;
        int validacion = 0;
        cedula = LabelCedula.getText().trim();
        nombres = txtNombres.getText();
        apellidos = txtApellidos.getText();
        edad = txtEdad.getText();
        correo = txtCorreo.getText();
        fechaact = fechas;
        usuarios = usuario;

        if (nombres.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese los nombres por favor");
            JBNombres.setVisible(true);
            validacion++;
        }
        if (apellidos.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese los apellidos por favor");
            JBApellidos.setVisible(true);
            validacion++;
        }
        if (edad.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese la edad por favor");
            JBCorreo.setVisible(true);
            validacion++;
        }
        if (correo.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese el correo por favor");
            JBEdad.setVisible(true);
            validacion++;
        }
        if (validacion == 0) {
            JBClientes.setVisible(true);
            Vector<String> datos = new Vector<>();
            datos.add(cedula);
            datos.add(nombres);
            datos.add(apellidos);
            datos.add(edad);
            datos.add(correo);
            datos.add(fechaact);
            datos.add(usuarios);

            matriz.add(datos);
        } else {
            JOptionPane.showMessageDialog(this, "Debe Ingresar todos los datos");
            JBClientes.setVisible(false);
        }

        OperarcionesCRUD op = OperarcionesCRUD.getInstance();

        try {
            op.ActulizarDatoS(matriz);

        } catch (SQLException err) {
            err.printStackTrace();
        }


    }//GEN-LAST:event_JBActualizarActionPerformed

