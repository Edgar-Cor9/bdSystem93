    private boolean cerrado(Object obj) {
        JInternalFrame[] FrmActivo = JDesktopCliente.getAllFrames();
        boolean cerrado = true;
        int i = 0;
        while (i < FrmActivo.length && cerrado) {
            if (FrmActivo[i] == obj) {
                cerrado = false;
            }
            i++;
        }
        return cerrado;
    }

